MATERIAL VARCHAR(30),
PESO NUMERIC(6,2),
ALTURA NUMERIC(5,2),
CODIGO INTEGER PRIMARY KEY);

CREATE TABLE OBRAS (
CODIGO INTEGER PRIMARY KEY,
TITULO VARCHAR(50),
ANO INTEGER,
SALAO INTEGER,
AUTOR INTEGER );

CREATE TABLE ALOCACOES (
HORA_ENTRADA CHAR(5),
HORA_SAIDA CHAR(5),
NUMERO INTEGER,
CPF CHAR(11),
PRIMARY KEY(NUMERO,CPF) );

CREATE TABLE SALOES (
