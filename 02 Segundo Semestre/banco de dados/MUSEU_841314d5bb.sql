NOME VARCHAR(50),
NACIONALIDADE VARCHAR(50));

CREATE TABLE TURNOS (
CODTURNO CHAR(1) PRIMARY KEY,
NOMETURNO VARCHAR(5));

CREATE TABLE FUNCIONARIOS (
TURNO CHAR(1),
FUNCAO VARCHAR(20),
NOME VARCHAR(50),
SALARIO NUMERIC(7,2),
CPF CHAR(11) PRIMARY KEY);

